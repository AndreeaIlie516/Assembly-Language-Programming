DAT SEGMENT PARA PUBLIC 'DATA'
STRING DW 4,7,13,2
LENGHT DW ($-STRING)/2
DAT ENDS

COD1 SEGMENT PARA PUBLIC 'CODE'
EXTRN SUM:NEAR
MAIN PROC FAR
ASSUME CS:COD1, DS:DAT, ES:DAT, SS:MYSTACK
PUSH DS
XOR AX,AX
PUSH AX
MOV AX,DAT
MOV DS,AX
